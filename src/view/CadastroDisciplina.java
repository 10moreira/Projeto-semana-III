    //Método que atualiza a tabela disciplina quando é realizada alguma ação sobre ela
        public void atualizaTabelaDisciplina() {
        listDisciplina.clear();
        listDisciplina.addAll(daodisciplina.getLista());
        int linha = listDisciplina.size() - 1;
        if (linha >= 0) {
            jTableDisciplinas.setRowSelectionInterval(linha, linha);
            jTableDisciplinas.scrollRectToVisible(
                    jTableDisciplinas.getCellRect(linha, linha, true));
        }
        
      }
        //Método que atualiza a tabela Cursos quando é realizada alguma ação sobre ela    
        public void atualizaTabelaCursos() {
        listCursos.clear();
        listCursos.addAll(daocurso.getLista());
        int linha = listCursos.size() - 1;
        if (linha >= 0) {
            jTableCursos.setRowSelectionInterval(linha, linha);
            jTableCursos.scrollRectToVisible(
                    jTableCursos.getCellRect(linha, linha, true));
        }
    }
    
    
