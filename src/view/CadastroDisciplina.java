    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     //Botão que exclui um registro da tabela trazendo uma JOptionPane para a confirmação da exclusão
        int opcao 
               = JOptionPane.showOptionDialog(null,
                        "Confirma a exclusão?",
                        "Pergunta",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE, null,
                        new String[]{"Sim", "Não"}, "Sim");
       
        if (opcao == 0) {
            int linhaSelecionada = jTableDisciplinas.getSelectedRow();
            Disciplina obj = listDisciplina.get(linhaSelecionada);
            daodisciplina.remover(obj);
            atualizaTabelaDisciplina();
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

